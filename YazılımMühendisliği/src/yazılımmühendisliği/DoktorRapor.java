    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Rapor r = new Rapor();
            r.setRaporid(Integer.parseInt(jTextField8.getText()));
            r.setRaportarih(jTextField7.getText());
            r.setRvdoktor(DoktorGirisEkrani.drGonder);
            r.setRadsoyad(jTextField6.getText());
            r.setRtcno(jTextField5.getText());
            r.setRbabaadi(jTextField4.getText());
            r.setRdtarih(jTextField3.getText());
            r.setRdyer(jTextField2.getText());
            r.setRistekneden(jTextField1.getText());
            r.setRaciklama(jTextArea1.getText());
            
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("YazilimMuhPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();
            em.persist(r);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Rapor Oluşturuldu.");
            this.setVisible(false);
            em.close();
            emf.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Rapor Oluşturulamadı.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

