    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Sevk s = new Sevk();
            s.setSevkid(Integer.parseInt(jTextField1.getText()));
            s.setSadsoyad(jTextField2.getText());
            s.setStcno(jTextField3.getText());
            s.setSdtarih(jTextField4.getText());
            s.setSdyer(jTextField5.getText());
            s.setSevktarihi(jTextField6.getText());
            s.setSevkedenkurum(jTextField7.getText());
            s.setSevkedendr(jTextField8.getText());
            s.setSevkedilenkur(jTextField9.getText());
            s.setSneden(jTextArea1.getText());

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("YazilimMuhPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();
            em.persist(s);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Sevk Oluşturuldu.");
            this.setVisible(false);
            em.close();
            emf.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sevk Oluşturulamadı.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

