    void sevkID() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("YazilimMuhPU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT MAX(p.sevkid) FROM Sevk p");

        int d = (int) q.getResultList().get(0);
        jTextField1.setText(String.valueOf(d + 1));

        em.close();
        emf.close();
    }

    void Doldur() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("YazilimMuhPU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT p FROM Hkayit p WHERE p.htcno = :tcno");
        q.setParameter("tcno", DoktorGirisEkrani.tcGonder);
        List<Hkayit> kisiList = q.getResultList();
        for (Hkayit tc : kisiList) {
            jTextField2.setText(tc.getHadsoyad());
            jTextField3.setText(tc.getHtcno());
            jTextField4.setText(tc.getHdtarih());
            jTextField5.setText(tc.getHdyer());
            jTextField7.setText("Şifa Polikliniği");
            jTextField8.setText(DoktorGirisEkrani.drGonder);
        }
        em.close();
        emf.close();
    }

